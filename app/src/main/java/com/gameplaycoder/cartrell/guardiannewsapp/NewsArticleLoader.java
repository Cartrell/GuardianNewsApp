package com.gameplaycoder.cartrell.guardiannewsapp;

import android.content.AsyncTaskLoader;
import android.content.Context;

import java.util.List;

public class NewsArticleLoader extends AsyncTaskLoader<List<NewsArticle>> {
  /////////////////////////////////////////////////////////////////////////////////////////
  //=======================================================================================
  // members
  //=======================================================================================
  /////////////////////////////////////////////////////////////////////////////////////////
  private String mUrl;

  //=======================================================================================
  // loadInBackground
  //=======================================================================================
  @Override
  public List<NewsArticle> loadInBackground() {
    if (mUrl == null) {
      return(null);
    }

    return(BackendUtils.FetchNewsArticles(mUrl));
  }

  /////////////////////////////////////////////////////////////////////////////////////////
  //=======================================================================================
  // package-private
  //=======================================================================================
  /////////////////////////////////////////////////////////////////////////////////////////

  //=======================================================================================
  // ctor
  //=======================================================================================
  NewsArticleLoader(Context context, String url) {
    super(context);
    mUrl = url;
  }


  //=======================================================================================
  // onStartLoading
  //=======================================================================================
  @Override
  protected void onStartLoading() {
    forceLoad();
  }
}
